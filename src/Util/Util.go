func ITF_toStr(info interface{}) string {

	if info != nil {
		if reflect.ValueOf(info).Kind() == reflect.Int32 {
			numero := int64(info.(int32))
			return strconv.FormatInt(numero, 10)
		} else {
			return fmt.Sprintf("%s", info)
		}
	} else {
		return ""
	}
}

